
package Entidad;

/**
 *
 * @author Usuario
 */
public class Raices {
    /*atributos*/
    private int a;
    private int b;
    private int c;
    
    /*constructor vacio*/
    public Raices() {
    }
    
    /*constructor por parametro*/
    public Raices(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
    
    /*getter*/
    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
    
    
    /*setter*/
    public void setA(int a) {
        this.a = a;
    }

    public void setB(int b) {
        this.b = b;
    }

    public void setC(int c) {
        this.c = c;
    }
    
    
}
